# Вік Тетянки
x = int(input("Введіть вік Тетянки: "))

# Вік Міті
y = int(input("Введіть вік Міті: "))

# Середній вік
average = (x + y) / 2

# Різниця між віком Тетянки та середнім значенням
difference_tetyana = x - average

# Різниця між віком Міті та середнім значенням
difference_mitya = y - average

# Вивід результатів
print("Середній вік:", average)
print("Різниця між віком Тетянки та середнім значенням:", difference_tetyana)
print("Різниця між віком Міті та середнім значенням:", difference_mitya)
